class Dog:
    def __init__(self, name, age, master):
        self.name = name
        self.age = age
        self.master = master

    def bark(self):
        print('bark')

    def run(self):
        print('run')

    def jump(self):
        print('jump')

    def sleep(self):
        print('sleep')

    def birthday(self):
        self.age += 1


