class Math:
    def __init__(self, num1, num2):
        number1 = num1
        number2 = num2

    @staticmethod
    def add(a, b):
        return a + b

    @staticmethod
    def diff(a, b):
        return a - b

    @staticmethod
    def mul(a, b):
        return a * b

    @staticmethod
    def div(a, b):
        return a / b
